package com.validic.json.objects;

/**
 *
 * @author Duane DeCouteau
 */
public class Routine {
    private String id;
    private String timeStamp;
    private String utcOffset;
    private String steps;
    private String distance;
    private String miles;
    private String floors;
    private String elevation;
    private String calories_burned;
    private String source;
    private String sourceName;
    private String lastModified;
    private String dayOfWeek;
    

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the timeStamp
     */
    public String getTimeStamp() {
        return timeStamp;
    }

    /**
     * @param timeStamp the timeStamp to set
     */
    public void setTimeStamp(String timeStamp) {
        this.timeStamp = timeStamp;
    }

    /**
     * @return the utcOffset
     */
    public String getUtcOffset() {
        return utcOffset;
    }

    /**
     * @param utcOffset the utcOffset to set
     */
    public void setUtcOffset(String utcOffset) {
        this.utcOffset = utcOffset;
    }

    /**
     * @return the steps
     */
    public String getSteps() {
        return steps;
    }

    /**
     * @param steps the steps to set
     */
    public void setSteps(String steps) {
        this.steps = steps;
    }

    /**
     * @return the distance
     */
    public String getDistance() {
        return distance;
    }

    /**
     * @param distance the distance to set
     */
    public void setDistance(String distance) {
        this.distance = distance;
    }

    /**
     * @return the floors
     */
    public String getFloors() {
        return floors;
    }

    /**
     * @param floors the floors to set
     */
    public void setFloors(String floors) {
        this.floors = floors;
    }

    /**
     * @return the elevation
     */
    public String getElevation() {
        return elevation;
    }

    /**
     * @param elevation the elevation to set
     */
    public void setElevation(String elevation) {
        this.elevation = elevation;
    }

    /**
     * @return the calories_burned
     */
    public String getCalories_burned() {
        return calories_burned;
    }

    /**
     * @param calories_burned the calories_burned to set
     */
    public void setCalories_burned(String calories_burned) {
        this.calories_burned = calories_burned;
    }

    /**
     * @return the source
     */
    public String getSource() {
        return source;
    }

    /**
     * @param source the source to set
     */
    public void setSource(String source) {
        this.source = source;
    }

    /**
     * @return the sourceName
     */
    public String getSourceName() {
        return sourceName;
    }

    /**
     * @param sourceName the sourceName to set
     */
    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }

    /**
     * @return the lastModified
     */
    public String getLastModified() {
        return lastModified;
    }

    /**
     * @param lastModified the lastModified to set
     */
    public void setLastModified(String lastModified) {
        this.lastModified = lastModified;
    }

    /**
     * @return the dayOfWeek
     */
    public String getDayOfWeek() {
        return dayOfWeek;
    }

    /**
     * @param dayOfWeek the dayOfWeek to set
     */
    public void setDayOfWeek(String dayOfWeek) {
        this.dayOfWeek = dayOfWeek;
    }

    /**
     * @return the miles
     */
    public String getMiles() {
        return miles;
    }

    /**
     * @param miles the miles to set
     */
    public void setMiles(String miles) {
        this.miles = miles;
    }
}
